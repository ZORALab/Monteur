package oshelper

import (
	"syscall"
	"unsafe"
)

func _termSize() (row uint, column uint) {
	size := &struct {
		Row    uint16
		Column uint16
	}{}

	_, _, _ = syscall.Syscall(syscall.SYS_IOCTL,
		uintptr(syscall.Stdin),
		uintptr(syscall.TIOCGWINSZ),
		uintptr(unsafe.Pointer(size)),
	)

	return uint(size.Row), uint(size.Column)
}
